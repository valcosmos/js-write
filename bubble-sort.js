const arr = [12, 2, 23, 43, 43, 5, 45, 4, 54, 65, 65656]

function bubbleSort(arr) {
  let temp

  for (let i = 0; i < arr.length - 1; i++) {
    for (let j = 0; j < arr.length - 1 - i; j++) {
      if (arr[j] > arr[j + 1]) {
        temp = arr[j]
        arr[j] = arr[j + 1]
        arr[j + 1] = temp
      }
    }
  }
  return arr
}

